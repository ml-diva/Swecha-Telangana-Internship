
// calculations using rust

fn main() {
     let a = 35;
     let b = 40;
     
     let x = a + b;
     println!("{}+{}={}",a,b,x);
     
     let y = a - b;
     println!("{}-{}={}",a,b,y);
     
     let z = a * b;
     println!("{}*{}={}",a,b,z);
     
     let e = a/b;
     println!("{}/{}={}",a,b,e);
     
}     
